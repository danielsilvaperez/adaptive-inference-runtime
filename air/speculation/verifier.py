"""
Token verification for speculative decoding.

This module implements token verification logic that checks whether draft
tokens generated by a small model would have been generated by the target
large model. This is the core of speculative decoding's correctness guarantee.
"""

from __future__ import annotations

from dataclasses import dataclass
from typing import TYPE_CHECKING

if TYPE_CHECKING:
    from air.interfaces.adapter import ModelAdapter
    from air.types import Token


@dataclass
class VerificationResult:
    """
    Result from verifying draft tokens.

    Attributes:
        accepted_tokens: List of draft tokens that were accepted.
        num_accepted: Number of tokens accepted.
        rejection_index: Index of first rejected token (-1 if all accepted).
        should_continue: Whether generation should continue after verification.
    """

    accepted_tokens: list[Token]
    num_accepted: int
    rejection_index: int
    should_continue: bool


class TokenVerifier:
    """
    Verifies draft tokens against target model predictions.

    The verifier uses the target model to check whether each draft token
    would have been generated given the prompt and previous tokens. This
    ensures that speculative decoding produces identical output to running
    the target model alone.

    Verification strategies:
        - Exact match: Draft token must exactly match target's argmax
        - Probabilistic: Draft token must be within target's probability distribution
        - Threshold: Draft token probability must exceed a threshold

    Example:
        >>> verifier = TokenVerifier(target_adapter, strategy="exact")
        >>> result = verifier.verify(
        ...     prompt="Hello",
        ...     draft_tokens=[Token(1, " world", -0.5), Token(2, "!", -0.3)]
        ... )
        >>> print(f"Accepted {result.num_accepted} tokens")
    """

